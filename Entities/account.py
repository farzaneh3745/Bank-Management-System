        self.id=id
        self.firstname=firstname
        self.lastname=lastname
        self.nationalcode=nationalcode
        self.accounttype=accounttype
        self.accountnumber=accountnumber
        self.openingdate=openingdate
        self.status=status
        self.balance=balance